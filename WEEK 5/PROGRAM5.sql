SELECT 
    E.ENAME,
    E.EMPNO,
    D.DNAME AS DEPARTMENT,
    A.JOB_ROLE,
    D.DLOC AS DEPT_LOCATION,
    P.PLOC AS PROJECT_LOCATION
FROM EMPLOYEE E
JOIN DEPT D ON E.DEPTNO = D.DEPTNO
JOIN ASSIGNED_TO A ON E.EMPNO = A.EMPNO
JOIN PROJECT P ON A.PNO = P.PNO
WHERE D.DLOC = P.PLOC;

